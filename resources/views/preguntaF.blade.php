  <li class="preguntas"><a href="#"><b>¿Que nombres se permiten en Artbook?</b></a></li>
  <p class="respuestas">Artbook es una comunidad en la que todas las personas usan el nombre que utilizan en su vida diaria. Esto se debe a que queremos asegurarnos de que sepas en todo momento con quien conectas.</p>
  El nombre no puede incluir:
    <p class="respuestas">1. Simbolos, numeros, mayusculas empleadas de modo inusual, signos de puntuacion o caracteres repetidos.</p>
    <p class="respuestas">2. Caracteres de varios idiomas.</p>
    <p class="respuestas">3. Titulos de cualquier tipo (por ejemplo, profesionales o reeligiosos).</p>
    <p class="respuestas">4. Palabras o frases en lugar de un nombre.</p>
    <p class="respuestas">5. Palabraas ofensivas o sugerentes de cualquier tipo.</p>
    <p class="respuestas">6. Si tu nombre cumple nuestras normas y aun asi tienes problemas para cambiarlo,<a href="#">descubre por que.</a></p>
