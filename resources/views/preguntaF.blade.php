  <li class="preguntas"><a href="#"><b>¿Puedo bloquear a alguien y como lo hago?</b></a></li>
  <p class="respuestas">Si bloqueas a un usuario, no podra ver a partir de ese momento las publicaciones de Artbook que compartes con el publico. Este usuario ya no sera su seguidor y tampoco lo estara siguiendo en Artbook. si quieres volver a seguir a una persona que has bloqueado, deberas desbloquearlo y enviarle una solicitud de seguidor.</p>

    <p class="respuestas">1. toca <span class="icon-menu"></span> en la parte superior derecha de Artbook.</p>
    <p class="respuestas">2. Desplazate hacia abajo y toca <b>configuracion</b>.</p>
    <p class="respuestas">3. Desplazate hacia abajo hasta <b>Privacidad</b> y toca <b>Bloqueos.</b></p>
    <p class="respuestas">4. Toca <b>Añadir a la lista de bloqueados</b>.</p>
    <p class="respuestas">5. Escribe el nombre de perfil de la persona que quieres bloquear.</p>
    <p class="respuestas">6. Entra a su perfil toca <b>Mas</b> y luego <b>Bloquear</b></p>
