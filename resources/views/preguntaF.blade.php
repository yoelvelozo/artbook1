  <li class="preguntas"><a href="#"><b>¿Como cambio o restablezco mi contraseña de Artbook?</b></a></li>
  <p class="respuestas">Para cambiar tu contraseña de Artbook cuando ya has iniciado sesion:</p>
    <p class="respuestas">1. Toca <span class="icon-menu"></span>en la parte superior derecha.</p>
    <p class="respuestas">2. Desplazate hacia abajo y toca <b>Configuracion</b>><b>Seguridad e inicio de sesion</b>><b>Cambiar contraseña</b>.</p>
    <p class="respuestas">3. Escribe la contraseña actual y la nueva y toca <b>Guardar cambios</b>.</p>
    <p class="respuestas">4. Si has iniciado sesion pero has olvidado tu contraseña, sigue los pasos que se indican en <b>Cambiar la contraseña</b>; a continuacion, toca <b>¿Has olvidado tu contraseña?</b> y sigue los pasos para restablecerla. Recuerda que necesitaras acceder al correo electronico asociado a tu cuenta.</p>
    <p class="respuestas">5. Cambiar tu contraseña</p>
    <p class="respuestas">Para restablecer tu contraseña sin haber iniciado sesion en Artbook:</p>
    <p class="respuestas">1. Toca <b>¿has olvidado la contraseña?</b>.</p>
    <p class="respuestas">2. Sigue las instrucciones que aparecen en la pantalla.</p>
    <p class="respuestas">3. Si sigues teniendo problemas, podemos ayudarte a <a href="#">recuperar tu cuenta.</a></p>
